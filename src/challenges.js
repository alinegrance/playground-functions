function highestCount(numbers) {
  let highestNum = numbers[0];
  let counter = 0;
  for (let i = 1; i < numbers.length; i += 1) {
    if (numbers[i] > highestNum) {
      highestNum = numbers[i];
    }
  }
  for (let j = 0; j < numbers.length; j += 1) {
    if (numbers[j] === highestNum) {
      counter += 1;
    }
  }
  return counter;
}

